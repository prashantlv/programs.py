class Solution:
    
    #Function to check whether there is a subarray present with 0-sum or not.
    def subArrayExists(self,arr,n):
        ##Your code here
        #Return true or false
        sum, flag = 0, 'No'
        for i in range(n):
            for j in range(i,n):
                sum += arr[j]
                if sum == 0:
                    flag = 'Yes'
        return flag

